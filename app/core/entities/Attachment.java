package core.entities;

public class Attachment {
	private Long id;
	private String fileName;
	private String uuid;
	private String thumbnailWidth;
	private String thumbnailHeight;
	private String fileSize;
	private String contentType;
	private Byte docType;
	private Boolean isThumbnail;
	private String docTypeText;
	private Boolean isPrimary;
	private Integer createdById;

	public Attachment() {

	}

	public Attachment(Long id, String fileName, String fileSize, String thumbnailWidth, String thumbnailHeight, 
			Byte docType, Boolean isPrimary, Integer createdById) {
		this.id = id;
		this.fileName = fileName;
		this.fileSize = fileSize;
		this.thumbnailWidth = thumbnailWidth;
		this.thumbnailHeight = thumbnailHeight;
		this.docType = docType;
		this.isPrimary =isPrimary;
		this.createdById=createdById;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}

	public String getThumbnailWidth() {
		return thumbnailWidth;
	}

	public void setThumbnailWidth(String thumbnailWidth) {
		this.thumbnailWidth = thumbnailWidth;
	}

	public String getThumbnailHeight() {
		return thumbnailHeight;
	}

	public void setThumbnailHeight(String thumbnailHeight) {
		this.thumbnailHeight = thumbnailHeight;
	}

	public String getFileSize() {
		return fileSize;
	}

	public void setFileSize(String fileSize) {
		this.fileSize = fileSize;
	}

	public String getContentType() {
		return contentType;
	}

	public void setContentType(String contentType) {
		this.contentType = contentType;
	}	

	public Byte getDocType() {
		return docType;
	}

	public void setDocType(Byte docType) {
		this.docType = docType;
	}	

	public Boolean getIsThumbnail() {
		return isThumbnail;
	}

	public void setIsThumbnail(Boolean isThumbnail) {
		this.isThumbnail = isThumbnail;
	}

	public String getDocTypeText() {
		return docTypeText;
	}
	public void setDocTypeText(String docTypeText) {
		this.docTypeText = docTypeText;
	}	
	
	public Boolean getIsPrimary() {
		return isPrimary;
	}

	public void setIsPrimary(Boolean isPrimary) {
		this.isPrimary = isPrimary;
	}
	
	

	public final Integer getCreatedById() {
		return createdById;
	}

	public final void setCreatedById(Integer createdById) {
		this.createdById = createdById;
	}

	@Override
	public String toString() {
		return "Attachment [id=" + id + ", fileName=" + fileName + ", uuid=" + uuid + ", thumbnailWidth="
				+ thumbnailWidth + ", thumbnailHeight=" + thumbnailHeight + ", fileSize=" + fileSize + ", contentType="
				+ contentType + "]";
	}

}
